def add_outline(img, color=(15, 15, 25, 255)):
    alpha = img.split()[3]
    dilated = alpha.filter(ImageFilter.MaxFilter(3))
    outline = Image.new('RGBA', img.size, color)
    outline.putalpha(dilated)
    return Image.alpha_composite(outline, img)


def save(img, category, name):
    img.save(os.path.join(OUT, category, f'{name}.png'))
    big = img.resize((img.width * 4, img.height * 4), Image.NEAREST)
    big.save(os.path.join(OUT, category, f'{name}_x4.png'))


