import os
from PIL import Image, ImageDraw, ImageFilter

OUT = os.path.join(os.path.dirname(__file__), 'compare_versions')
os.makedirs(OUT, exist_ok=True)

W, H = 96, 96


def add_outline(img, color=(15, 15, 25, 255)):
    """給任何 alpha 圖加一圈 1px 深色描邊（pixel art 經典做法）"""
    alpha = img.split()[3]
    dilated = alpha.filter(ImageFilter.MaxFilter(3))
    outline = Image.new('RGBA', img.size, color)
    outline.putalpha(dilated)
    return Image.alpha_composite(outline, img)


def save(img, name, scale=4):
    img.save(os.path.join(OUT, f'{name}.png'))
    big = img.resize((img.width * scale, img.height * scale), Image.NEAREST)
    big.save(os.path.join(OUT, f'{name}_x4.png'))


