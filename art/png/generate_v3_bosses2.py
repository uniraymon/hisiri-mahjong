OUT = os.path.join(os.path.dirname(__file__), 'modern_v3', 'bosses')
os.makedirs(OUT, exist_ok=True)

W, H = 128, 128


def add_outline(img, color=(15, 15, 25, 255)):
    alpha = img.split()[3]
    dilated = alpha.filter(ImageFilter.MaxFilter(3))
    outline = Image.new('RGBA', img.size, color)
    outline.putalpha(dilated)
    return Image.alpha_composite(outline, img)


def new_layer():
    return Image.new('RGBA', (W, H), (0, 0, 0, 0))


def save(img, name):
    img.save(os.path.join(OUT, f'{name}.png'))
    big = img.resize((W * 4, H * 4), Image.NEAREST)
    big.save(os.path.join(OUT, f'{name}_x4.png'))


def dark_circle_bg(color=(20, 5, 40)):
    """深色圓形背景（給世界王用）"""
    img = new_layer()
    d = ImageDraw.Draw(img)
    # 漸層深色圓
    cx, cy = 64, 64
    for r in range(62, 30, -2):
        alpha = int(180 * (1 - (62 - r) / 32))
        d.ellipse([cx-r, cy-r, cx+r, cy+r], fill=color + (alpha,))
    return img


